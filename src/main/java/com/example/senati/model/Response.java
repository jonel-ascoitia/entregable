package com.example.senati.model;

public class Response {

    private int code;
    private String stastus;
    private String meseng;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getStastus() {
        return stastus;
    }

    public void setStastus(String stastus) {
        this.stastus = stastus;
    }

    public String getMeseng() {
        return meseng;
    }

    public void setMeseng(String meseng) {
        this.meseng = meseng;
    }
}
